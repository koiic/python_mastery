# bitwise (and) -> &
# bitwise (or) -> |
# bitwise (Exclusive-or) XOR -> ^
# bitwise (not) -> ~
# bitwise (left shift) -> <<
# bitwise (right shift) -> >>

# Python program to show
# bitwise operators

a = 10
b = 4

# Print bitwise AND operation
print("a & b =", a & b)

# Print bitwise OR operation
print("a | b =", a | b)

# Print bitwise NOT operation
print("~a =", ~a)

# print bitwise XOR operation
print("a ^ b =", a ^ b)